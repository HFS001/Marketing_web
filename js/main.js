const i18nElements = document.querySelectorAll('[data-i18n]');
const placeholderElements = document.querySelectorAll('[data-i18n-placeholder]');
const langButtons = document.querySelectorAll('.lang-btn');
const form = document.getElementById('contact-form');
const formResponse = document.getElementById('form-response');
let currentLang = 'en';

const setDirection = (lang) => {
  const isRTL = lang === 'fa';
  document.body.setAttribute('dir', isRTL ? 'rtl' : 'ltr');
  document.body.classList.toggle('rtl', isRTL);
  document.documentElement.setAttribute('lang', lang === 'pt' ? 'pt-PT' : lang);
};

const updateResponseLocale = () => {
  if (!formResponse) return;
  const responseKey = formResponse.dataset.responseKey;
  if (responseKey && translations[currentLang][responseKey]) {
    formResponse.textContent = translations[currentLang][responseKey];
  }
};

const applyLanguage = (lang) => {
  const copy = translations[lang];
  if (!copy) return;

  currentLang = lang;
  setDirection(lang);

  i18nElements.forEach((el) => {
    const key = el.dataset.i18n;
    if (!key || typeof copy[key] === 'undefined') return;
    el.textContent = copy[key];
  });

  placeholderElements.forEach((el) => {
    const key = el.dataset.i18nPlaceholder;
    if (!key || typeof copy[key] === 'undefined') return;
    el.placeholder = copy[key];
  });

  document.title = copy.pageTitle || document.title;

  langButtons.forEach((btn) => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });

  updateResponseLocale();
};

langButtons.forEach((btn) => {
  btn.addEventListener('click', () => {
    const selected = btn.dataset.lang;
    if (selected === currentLang) return;
    applyLanguage(selected);
  });
});

const scrollLinks = document.querySelectorAll('a[href^="#"]');
scrollLinks.forEach((link) => {
  link.addEventListener('click', (event) => {
    const targetId = link.getAttribute('href');
    if (!targetId || targetId === '#') return;
    const target = document.querySelector(targetId);
    if (!target) return;
    event.preventDefault();
    target.scrollIntoView({ behavior: 'smooth', block: 'start' });
  });
});

const showFormResponse = (key) => {
  if (!formResponse) return;
  const message = translations[currentLang][key];
  if (!message) return;
  formResponse.textContent = message;
  formResponse.dataset.responseKey = key;
};

if (form) {
  form.addEventListener('submit', (event) => {
    event.preventDefault();
    form.reset();
    showFormResponse('formSuccess');
  });
}

applyLanguage(currentLang);
